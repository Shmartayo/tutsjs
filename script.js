/* 
    Conditionals
    Making decisions with code
*/

let score = 40;

if (score >= 90){
    console.log("You got A++");
} else if(score >= 80){
    console.log("You got A+");
} else if(score >= 70){
    console.log("You got A-");
} else if(score >= 60){
    console.log("You got B");
} else if(score >= 50){
    console.log("You got c");
} else {
    console.log("You got an F");
}
